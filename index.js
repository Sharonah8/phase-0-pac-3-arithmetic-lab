function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function increment(n) {
    return n + 1;
}

function decrement(n) {
    return n - 1;
}

function makeInt(n) {
    let x = parseInt(n, 10);
    return x;
}

function preserveDecimal(n) {
    let y = parseFloat(n);
    return y;
}